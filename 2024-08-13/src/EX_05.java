
public class EX_05 {

	public static void main(String[] args) {
		
		double pi = 3.14159;
		double raio = 7;
		double altura = 20; 
		double vol;
		
		vol = pi * raio * raio * altura; 
		
		System.out.println("Valor do raio: " + raio);
		System.out.println("Valor da altura: " + altura);
		System.out.println("Volume da lata: " + vol);

	}

}
